"""Offline sector classifier for every NSE equity ever carried by nse-universe.

Runs ONCE (rerun quarterly / when new IPOs enter the top-500 rank window) and
writes ``stock-sectors.json`` which the live + backtest paths read at startup.
No runtime external-data dependency.

Classification hierarchy:

1. **Authoritative** — whatever ``stock-universe.json`` already says for its
   204 hand-curated names and registered hedges.
2. **LLM-known** — an explicit dict of well-known NSE tickers classified by
   domain knowledge (main body of this file, ~800 entries).
3. **Heuristics** — regex patterns on the ticker (e.g. ``*BANK*`` → FINANCIALS).
4. **Unclassified** — everything else. The strategy treats UNCLASSIFIED like
   its own 1-stock sector (no caps, no sector-momentum penalty).

Run:
    .venv/bin/python tools/build_sectors.py

Writes ``stock-sectors.json`` at the repo root.
"""

