def _compiled_rules():
    return [(re.compile(p, re.IGNORECASE), s, ss) for p, s, ss in _HEURISTIC_RULES]


_COMPILED_RULES = _compiled_rules()


def _classify_via_heuristics(symbol: str) -> Tuple[str, str] | None:
    for rx, sector, sub in _COMPILED_RULES:
        if rx.search(symbol):
            return (sector, sub)
    return None


def _load_universe_json_sectors() -> Dict[str, Tuple[str, str]]:
    """Authoritative sector labels from the hand-curated stock-universe.json."""
    data = json.loads(UNIVERSE_JSON.read_text())
    out: Dict[str, Tuple[str, str]] = {}
    for uni_name, uni in data.get("universes", {}).items():
        for s in uni.get("stocks", []):
            out[s["ticker"]] = (s["sector"], s.get("sub_sector", s["sector"]))
    # Hedges too.
    for key, h in data.get("hedges", {}).items():
        sym = h.get("symbol")
        if not sym:
            continue
        mapping = {
            "gold": ("COMMODITIES", "GOLD_ETF"),
            "cash": ("DEBT", "LIQUID_ETF"),
        }
        out[sym] = mapping.get(key, ("DEFENSIVE", "DEFENSIVE"))
    return out


def _enumerate_all_nse_symbols() -> list[str]:
    """Every distinct NSE EQ symbol the nse-universe data has ever seen."""
    import duckdb
    # Canonical vendored parquet dir (honours NSE_UNIVERSE_DATA_DIR, defaults
    # to the in-repo data/parquet via nse_universe.paths).
    from nse_universe.paths import PARQUET_DIR
    nse_data_dir = PARQUET_DIR
    pattern = str(nse_data_dir / "year=*/month=*/*.parquet")
    con = duckdb.connect()
    df = con.execute(
        f"SELECT DISTINCT symbol FROM read_parquet('{pattern}') ORDER BY symbol"
    ).df()
    return df["symbol"].tolist()


def _load_nse_universe_sectors() -> Dict[str, Tuple[str, str]]:
    """Load NSE's authoritative sector data from nse-universe's sectors.parquet.

    Falls back to empty dict if the sectors module isn't available or the
    parquet file hasn't been generated yet. Run
    `python -c "from nse_universe import sectors; sectors.fetch_and_store()"`
    in the nse-universe repo to regenerate.
    """
    try:
        from nse_universe import sectors as _sectors
    except ImportError:
        print("[info] nse_universe.sectors not available — skipping NSE source", file=sys.stderr)
        return {}
    df = _sectors.all_classifications()
    if df.empty:
        print("[info] NSE sectors parquet empty — run sectors.fetch_and_store()", file=sys.stderr)
        return {}
    return {
        row["symbol"]: (row["sector"], row["sub_sector"])
        for _, row in df.iterrows()
        if row["sector"] != "UNCLASSIFIED"
    }


def build(output: Path = OUTPUT_PATH) -> None:
    authoritative = _load_universe_json_sectors()
    nse_sectors = _load_nse_universe_sectors()
    all_symbols = _enumerate_all_nse_symbols()

    out: Dict[str, dict] = {}
    source_counts = {
        "known_etf": 0,
        "universe_json": 0,
        "nse_authoritative": 0,
        "llm_map": 0,
        "heuristic": 0,
        "unclassified": 0,
    }

    # Priority: known_etf (guarantees non-equity ETFs can never slip into
    # eligible universe) > universe_json (hand-curated core) > NSE authoritative
    # > LLM map > heuristics. NSE covers the ~754 equity names; LLM+heuristics
    # fill the deep tail and historical delisted names NSE doesn't carry.
    for sym in all_symbols:
        if sym in _KNOWN_ETFS:
            sec, sub = _KNOWN_ETFS[sym]
            src = "known_etf"
        elif sym in authoritative:
            sec, sub = authoritative[sym]
            src = "universe_json"
        elif sym in nse_sectors:
            sec, sub = nse_sectors[sym]
            src = "nse_authoritative"
        elif sym in _LLM_SECTOR_MAP:
            sec, sub = _LLM_SECTOR_MAP[sym]
            src = "llm_map"
        else:
            hit = _classify_via_heuristics(sym)
            if hit is not None:
                sec, sub = hit
                src = "heuristic"
            else:
                sec, sub = ("UNCLASSIFIED", "UNCLASSIFIED")
                src = "unclassified"

        if sec not in SECTORS:
            print(f"[warn] unknown sector {sec!r} for {sym}, forcing UNCLASSIFIED", file=sys.stderr)
            sec, sub = ("UNCLASSIFIED", "UNCLASSIFIED")
            src = "unclassified"

        # Consolidate fragmented sub_sector labels to tight canonical vocabulary
        sub = _normalize_sub_sector(sub)

        out[sym] = {"sector": sec, "sub_sector": sub, "source": src}
        source_counts[src] += 1

    payload = {
        "generated_at": date.today().isoformat(),
        "total_symbols": len(out),
        "source_counts": source_counts,
        "coverage_pct": round(100 * (1 - source_counts["unclassified"] / len(out)), 2),
        "symbols": out,
    }
    output.write_text(json.dumps(payload, indent=2, sort_keys=True))

    print(f"Wrote {output} — {len(out):,} symbols")
    for src, count in source_counts.items():
        print(f"  {src:16s} {count:>5,}  ({100*count/len(out):.1f}%)")
    print(f"  coverage:        {payload['coverage_pct']:.1f}%")


if __name__ == "__main__":
    build()
